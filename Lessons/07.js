// typeof operator 

// data types (primitive data types)
// string "harhit"
// number 2, 4, 5.6 
// booleans 
// undefined
// null 
// BigInt
// Symbol

let age = 25;
let fname = "Rishit";

console.log(typeof age);   // number
console.log(typeof fname);  // string


// convert number to string

// or age = string(age);
console.log(typeof (age + ""));  // adding "" after the number make it a sting


// convert string to number
 
// or fname = number(fname);
let mynum = +"Rahul";  // adding + before a string make it a number

console.log(typeof mynum);